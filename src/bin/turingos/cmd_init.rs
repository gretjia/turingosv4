#[derive(Debug)]
enum InitError {
    /// Project path already exists as a regular file (not a directory).
    ProjectIsFile(String),
    /// Project directory already exists and is non-empty; `--force` not provided.
    ProjectDirExists(String),
    /// Filesystem I/O error.
    Io(String),
}

impl InitError {
    fn exit_code(&self) -> u8 {
        match self {
            // Domain errors (user intent conflict): exit 1
            Self::ProjectIsFile(_) | Self::ProjectDirExists(_) => 1,
            // Unexpected I/O: exit 2
            Self::Io(_) => 2,
        }
    }
}

impl std::fmt::Display for InitError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::ProjectIsFile(p) => write!(
                f,
                "path exists but is a regular file (not a directory): {p} — pick a different --project path"
            ),
            Self::ProjectDirExists(p) => write!(
                f,
                "project directory {p} already exists and is not empty — use --force to overwrite, or pick an empty dir"
            ),
            Self::Io(msg) => write!(f, "io error: {msg}"),
        }
    }
}

fn cmd_init_inner(args: InitArgs) -> Result<(), InitError> {
    let project_dir = PathBuf::from(&args.project);

    // Pre-flight classification: file vs directory vs absent.
    let project_dir_existed_before = project_dir.exists();
    if project_dir_existed_before {
        if project_dir.is_file() {
            // Regular file at the path → never safe to scaffold over,
            // not even with --force (we'd have to delete it).
            return Err(InitError::ProjectIsFile(args.project.clone()));
        }
        if !args.force {
            // Allow init into an empty existing directory without --force.
            let is_empty = std::fs::read_dir(&project_dir)
                .map(|mut entries| entries.next().is_none())
                .unwrap_or(false);
            if !is_empty {
                return Err(InitError::ProjectDirExists(args.project.clone()));
            }
        }
    }

    fs::create_dir_all(&project_dir)
        .map_err(|e| InitError::Io(format!("create project dir {}: {e}", project_dir.display())))?;
    fs::create_dir_all(project_dir.join("runtime_repo"))
        .map_err(|e| InitError::Io(format!("create runtime_repo: {e}")))?;
    fs::create_dir_all(project_dir.join("cas"))
        .map_err(|e| InitError::Io(format!("create cas: {e}")))?;

    let genesis_path = project_dir.join("genesis_payload.toml");
    let agent_pubkeys_path = project_dir.join("agent_pubkeys.json");
    let turingos_toml_path = project_dir.join("turingos.toml");

    fs::write(&genesis_path, args.template.content())
        .map_err(|e| InitError::Io(format!("write genesis_payload.toml: {e}")))?;
    fs::write(&agent_pubkeys_path, AGENT_PUBKEYS_PLACEHOLDER)
        .map_err(|e| InitError::Io(format!("write agent_pubkeys.json: {e}")))?;

    // Write turingos.toml with provider-appropriate defaults.
    // If the file already exists and --force is NOT set, leave it alone.
    let toml_written = if turingos_toml_path.exists() && !args.force {
        eprintln!("note: turingos.toml already exists; not overwriting (use --force).");
        false
    } else {
        let toml_content = match args.provider {
            Provider::SiliconFlow => toml_siliconflow(),
            Provider::DeepSeek => TOML_DEEPSEEK.to_string(),
        };
        fs::write(&turingos_toml_path, toml_content)
            .map_err(|e| InitError::Io(format!("write turingos.toml: {e}")))?;
        true
    };

    let cd_hint = shell_quote_path(&project_dir);
    let action_verb = if args.force && project_dir_existed_before {
        "Re-initialized"
    } else {
        "Initialized"
    };

    println!(
        "{action_verb} TuringOS workspace at {} (template: {})",
        project_dir.display(),
        args.template.name(),
    );
    if args.force && project_dir_existed_before {
        println!("(--force: overwrote canonical scaffold files; other files preserved)");
    }
    println!();
    println!("Scaffold files created:");
    println!("  {}", project_dir.join("runtime_repo").display());
    println!("  {}", project_dir.join("cas").display());
    println!("  {}", genesis_path.display());
    println!(
        "  {}  (map agent_id -> ed25519 pubkey; Phase 6.1+ schema)",
        agent_pubkeys_path.display()
    );
    if toml_written {
        println!("  {}", turingos_toml_path.display());
    }
    println!();

    match args.provider {
        Provider::SiliconFlow => {
            println!("Provider: SiliconFlow (api.siliconflow.cn)");
            println!("Next steps:");
            println!("  cd {cd_hint}");
            println!("  export SILICONFLOW_API_KEY=\"sk-...\"");
            println!(
                "  turingos spec --workspace {cd_hint}           # 8-question grill -> spec.md (CAS-anchored)"
            );
            println!(
                "  turingos generate --workspace {cd_hint}       # emit working code into artifacts/"
            );
        }
        Provider::DeepSeek => {
            println!("Provider: DeepSeek (api.deepseek.com)");
            println!("Next steps:");
            println!("  export DEEPSEEK_API_KEY=\"sk-...\"");
            println!("  export DEEPSEEK_API_KEY_WORKER=\"sk-...\"");
            println!(
                "  export TURINGOS_SILICONFLOW_ENDPOINT=\"https://api.deepseek.com/v1/chat/completions\""
            );
            println!("  turingos spec --workspace {cd_hint} --lang en --mode static");
        }
    }

    println!();
    println!("  turingos welcome --workspace {cd_hint}        # check progress anytime");
    println!();
    println!("Or, for the established multi-agent benchmark path (Phase 6.1):");
    println!("  turingos --help                         # full subcommand list");
    println!("  turingos agent deploy --id <ID> ...     # register an agent's pubkey + role");
    println!("  turingos config set <key> <value>       # workspace-local config");
    println!("  turingos task open --problem <ID> ...   # open a task on a fresh ChainTape");
    println!("  turingos task view --task-id <ID>       # replay task status");
    println!("  turingos report wallet                  # replay agent wallet balances");
    println!("  turingos audit dashboard                # regenerate audit views");
    println!("  turingos export evidence --source ...   # bundle an evidence directory");
    println!();
    println!("Task-type backends (Phase 6.1 ships the proof/Lean backend; more in Phase 7+):");
    println!("  # From the turingosv4 project root (NOT this scaffold):");
    println!("  cargo build --release --workspace --bins");
    println!(
        "  See: handover/research/interaction_substrate/50_deliverables/00_UNIFIED_CLI_SPEC.md"
    );

    Ok(())
}

